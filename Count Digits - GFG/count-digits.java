class Solution{
    static int evenlyDivides(int N){
        // code here
        int ans =0;
        int dup = N;
        while(dup>0){
        
            int x= dup%10;
            if(x!=0&&N%x==0)
            ans++;
            dup=dup/10;
        }
        return ans;
    }
}